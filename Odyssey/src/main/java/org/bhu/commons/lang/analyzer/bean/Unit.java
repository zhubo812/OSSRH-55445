package org.bhu.commons.lang.analyzer.bean;

public class Unit {

	int firstword;
	int secondword;
	
	Lexicon lexicon;

	public int getFirstword() {
		return firstword;
	}

	public void setFirstword(int firstword) {
		this.firstword = firstword;
	}

	public int getSecondword() {
		return secondword;
	}

	public void setSecondword(int secondword) {
		this.secondword = secondword;
	}

	public Lexicon getLexicon() {
		return lexicon;
	}

	public void setLexicon(Lexicon lexicon) {
		this.lexicon = lexicon;
	}
	
	
	
}
